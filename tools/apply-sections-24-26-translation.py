def load_data() -> dict:
    source = ROUTE_DATA.read_text(encoding="utf-8")
    match = re.search(r"window\.ROUTE_DATA\s*=\s*(\{.*\});\s*$", source, re.S)
    if not match:
        raise RuntimeError("Could not parse route data")
    return json.loads(match.group(1))


def save_data(data: dict) -> None:
    payload = "window.ROUTE_DATA = " + json.dumps(data, ensure_ascii=False, indent=2) + ";\n"
    ROUTE_DATA.write_text(payload, encoding="utf-8")


def main() -> None:
    data = load_data()
    sections_by_id = {section["id"]: section for section in data["sections"]}

    for section_id, patch in SECTIONS.items():
        if section_id not in sections_by_id:
            raise RuntimeError(f"Missing section: {section_id}")
        section = sections_by_id[section_id]
        steps = patch["steps"]
        if len(section["steps"]) != len(steps):
            raise RuntimeError(f"{section_id}: expected {len(section['steps'])} translations, got {len(steps)}")

        section["title"] = patch["title"]
        section["level"] = patch["level"]
        section["region"] = patch["region"]
        for step, text in zip(section["steps"], steps):
            step["text"] = text
            step["translatedBy"] = TRANSLATED_BY

    save_data(data)
    print("Updated sections 24 through 26")


if __name__ == "__main__":
    main()
