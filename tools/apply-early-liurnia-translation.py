def load_route() -> dict:
    text = ROUTE_DATA.read_text(encoding="utf-8")
    match = re.search(r"window\.ROUTE_DATA\s*=\s*(.*);\s*$", text, re.S)
    if not match:
        raise RuntimeError("Could not parse route-data.js")
    return json.loads(match.group(1))


def save_route(data: dict) -> None:
    ROUTE_DATA.write_text("window.ROUTE_DATA = " + json.dumps(data, ensure_ascii=False, indent=2) + ";\n", encoding="utf-8")


def main() -> None:
    data = load_route()
    section = data["sections"][2]
    if section["id"] != "early-liurnia":
        raise RuntimeError(f"Expected early-liurnia section, got {section['id']}")
    if len(section["steps"]) != len(TRANSLATIONS):
        raise RuntimeError(f"Expected {len(TRANSLATIONS)} steps, got {len(section['steps'])}")

    section["title"] = "Ранняя Лиурния и Кэлид"
    section["region"] = "Лиурния, Кэлид, Крепость Круглого стола"
    section["level"] = "20-35"

    for step, text in zip(section["steps"], TRANSLATIONS):
        step["text"] = text
        step["translatedBy"] = "gpt-section-v3"

    data.setdefault("source", {})["sectionTranslation"] = "tutorial:gpt-section-v1;first-steps:gpt-section-v2;early-liurnia:gpt-section-v3"
    save_route(data)
    print(f"Updated early-liurnia section: {len(section['steps'])} steps")


if __name__ == "__main__":
    main()
