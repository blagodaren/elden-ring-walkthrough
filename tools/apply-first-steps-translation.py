def load_route() -> dict:
    text = ROUTE_DATA.read_text(encoding="utf-8")
    match = re.search(r"window\.ROUTE_DATA\s*=\s*(.*);\s*$", text, re.S)
    if not match:
        raise RuntimeError("Could not parse route-data.js")
    return json.loads(match.group(1))


def save_route(data: dict) -> None:
    ROUTE_DATA.write_text("window.ROUTE_DATA = " + json.dumps(data, ensure_ascii=False, indent=2) + ";\n", encoding="utf-8")


def polish(text: str) -> str:
    fixed = text
    for src, dst in REPLACEMENTS:
        fixed = fixed.replace(src, dst)
    fixed = re.sub(r"место место благодати", "место благодати", fixed)
    fixed = re.sub(r"место благодати «([^»]+)» место благодати", r"место благодати «\1»", fixed)
    fixed = fixed.replace("?.", ".")
    return fixed


def main() -> None:
    data = load_route()
    section = data["sections"][1]
    section["title"] = "Первые шаги в Замогилье"
    section["region"] = "Западное и северное Замогилье"
    section["level"] = "1-20"

    for index, step in enumerate(section["steps"]):
        if index in MANUAL:
            step["text"] = MANUAL[index]
            step["translatedBy"] = "gpt-section-v2-manual"
        else:
            step["text"] = polish(step["text"])
            step["translatedBy"] = "gpt-section-v2-polish"

    data.setdefault("source", {})["sectionTranslation"] = "tutorial:gpt-section-v1;first-steps:gpt-section-v2"
    save_route(data)
    print(f"Updated first-steps section: {len(section['steps'])} steps")


if __name__ == "__main__":
    main()
