def load_data() -> dict:
    text = ROUTE_DATA.read_text(encoding="utf-8")
    match = re.search(r"window\.ROUTE_DATA\s*=\s*(.*);\s*$", text, flags=re.S)
    if not match:
        raise ValueError("route-data.js does not contain window.ROUTE_DATA")
    return json.loads(match.group(1))


def save_data(data: dict) -> None:
    ROUTE_DATA.write_text(
        "window.ROUTE_DATA = " + json.dumps(data, ensure_ascii=False, indent=2) + ";\n",
        encoding="utf-8",
    )


def main() -> None:
    data = load_data()

    for expected_id, section_data in SECTIONS.items():
        section = data["sections"][section_data["index"]]
        if section["id"] != expected_id:
            raise ValueError(
                f"Section index {section_data['index']} is {section['id']!r}, expected {expected_id!r}"
            )

        translations = section_data["steps"]
        if len(section["steps"]) != len(translations):
            raise ValueError(
                f"{expected_id}: {len(section['steps'])} steps, {len(translations)} translations"
            )

        section["title"] = section_data["title"]
        section["level"] = section_data["level"]
        section["region"] = section_data["region"]

        for step, text in zip(section["steps"], translations):
            step["text"] = text
            step["translatedBy"] = TRANSLATED_BY

    save_data(data)


