def load_data() -> dict:
    source = ROUTE_DATA.read_text(encoding="utf-8")
    match = re.search(r"window\.ROUTE_DATA\s*=\s*(\{.*\});\s*$", source, re.S)
    if not match:
        raise RuntimeError("Could not parse route data")
    return json.loads(match.group(1))


def save_data(data: dict) -> None:
    ROUTE_DATA.write_text("window.ROUTE_DATA = " + json.dumps(data, ensure_ascii=False, indent=2) + ";\n", encoding="utf-8")


def polish_text(text: str) -> str:
    for source, target in PHRASE_REPLACEMENTS:
        text = text.replace(source, target)
    for source, target in STYLE_REPLACEMENTS:
        text = text.replace(source, target)

    text = re.sub(r"\((\d+)\)", r"[\1]", text)
    text = re.sub(r"\b(\d[\d ]*)\s+руны\b", r"\1 рун", text)
    text = re.sub(r"\b(\d[\d ]*)\s+руна\b", r"\1 рун", text)
    text = re.sub(r"\bблагодать\b", "место благодати", text)
    text = re.sub(r"\bБлагодать\b", "Место благодати", text)
    text = re.sub(r"место место благодати", "место благодати", text)
    text = re.sub(r"Место место благодати", "Место благодати", text)
    text = text.replace("к место благодати", "к месту благодати")
    text = text.replace("с место благодати", "с места благодати")
    text = text.replace("рядом с место благодати", "рядом с местом благодати")
    text = text.replace("у место благодати", "у места благодати")
    text = text.replace("от место благодати", "от места благодати")
    text = text.replace("перед место благодати", "перед местом благодати")
    text = text.replace("место благодати рядом", "места благодати рядом")
    text = re.sub(r"\s+", " ", text).strip()
    return text


def main() -> None:
    data = load_data()
    expected_ids = list(SECTION_META)
    sections = data["sections"][18:30]
    actual_ids = [section["id"] for section in sections]
    if actual_ids != expected_ids:
        raise RuntimeError(f"Unexpected section order: {actual_ids}")

    for section in sections:
        title, region, level, expected_count = SECTION_META[section["id"]]
        if len(section["steps"]) != expected_count:
            raise RuntimeError(f"{section['id']}: expected {expected_count}, got {len(section['steps'])}")

        section["title"] = title
        section["region"] = region
        section["level"] = level
        for step in section["steps"]:
            step["text"] = polish_text(step["text"])
            step["translatedBy"] = "gpt-batch-polish-v19-30"

    save_data(data)
    print("Updated sections 19 through 30")


if __name__ == "__main__":
    main()
